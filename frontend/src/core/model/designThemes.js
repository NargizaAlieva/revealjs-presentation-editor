export const applyColorSchemeToDesign = (design, colorScheme) => {
  if (!design || !design.decorations || !design.decorations.shapes) return design;

  const updatedShapes = design.decorations.shapes.map((shape) => {
    const colorIndex = shape.colorIndex ?? 0;
    const newColor = colorScheme.colors[colorIndex];

    if (!newColor) return shape;

    const updated = { ...shape };
    if (shape.fill && shape.fill !== "none") {
      updated.fill = newColor;
    }
    if (shape.stroke && shape.stroke !== "none") {
      updated.stroke = newColor;
    }
    return updated;
  });

  return {
    ...design,
    decorations: {
      ...design.decorations,
      shapes: updatedShapes,
    },
  };
};

export const updateThemeBackgroundFromScheme = (colorTheme, colorScheme) => {
  return (colorTheme ?? []).map((e) =>
    e["css-variable-name"] === "bg-light" ? { ...e, color: `${colorScheme.background}FF` } : e,
  );
};

export const updateThemeColor = (colorTheme, cssVariableName, newHex7) => {
  return (colorTheme ?? []).map((entry) => {
    if (entry["css-variable-name"] !== cssVariableName) return entry;
    const alpha =
      typeof entry.color === "string" && entry.color.length === 9
        ? entry.color.slice(7)
        : "FF";
    return { ...entry, color: `${newHex7}${alpha}` };
  });
};

